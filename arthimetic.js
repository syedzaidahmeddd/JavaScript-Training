// Arthimetic Basic Operations
// + , - , * , / , % , ++(increment) , --(decrement) , **(power operator/exponential operator) ;
// no equal to in arithmetic operations ex (+=)

let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a++);
console.log(a--);
console.log(a**b); // a power (b) a^b .. 10 power 5

//increment : increase the value by 1 ++
//decrement : decrease the value by 1 --

